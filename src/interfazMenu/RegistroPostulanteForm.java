    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     JFileChooser fileChooser = new JFileChooser();
    FileNameExtensionFilter filter = new FileNameExtensionFilter(
        "Imágenes (*.jpg, *.jpeg, *.png)", "jpg", "jpeg", "png");
    fileChooser.setFileFilter(filter);
    
    int opcion = fileChooser.showOpenDialog(this);
    if (opcion == JFileChooser.APPROVE_OPTION) {
        File archivo = fileChooser.getSelectedFile();
        try {
            // Cargar imagen
            ImageIcon icon = new ImageIcon(archivo.getAbsolutePath());
            
            // Obtener tamaño del JLabel; si es 0 usamos el preferredSize (evita escalado a 0)
            int w = lblFoto.getWidth();
            int h = lblFoto.getHeight();
            if (w <= 0 || h <= 0) {
                Dimension d = lblFoto.getPreferredSize();
                w = d.width;
                h = d.height;
            }

            // Escalar la imagen y asignarla al JLabel
            Image img = icon.getImage().getScaledInstance(w, h, Image.SCALE_SMOOTH);
            lblFoto.setIcon(new ImageIcon(img));
            lblFoto.setText(""); // quitamos el texto "Sin foto"

            // Guardar ruta si usas la variable de clase
            rutaFoto = archivo.getAbsolutePath();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                "Error al cargar la imagen: " + ex.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

