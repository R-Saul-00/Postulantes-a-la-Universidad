    private void agregarPlaceholder(JTextField campo, String placeholder) {
    // Texto inicial
    campo.setText(placeholder);
    campo.setForeground(Color.gray);

    campo.addFocusListener(new java.awt.event.FocusAdapter() {
        @Override
        public void focusGained(java.awt.event.FocusEvent evt) {
            if (campo.getText().equals(placeholder)) {
                campo.setText("");
                campo.setForeground(Color.black);
            }
        }

        @Override
        public void focusLost(java.awt.event.FocusEvent evt) {
            if (campo.getText().isEmpty()) {
                campo.setText(placeholder);
                campo.setForeground(Color.gray);
            }
        }
    });
}
    private void agregarPlaceholderContrasena(JPasswordField campo, String placeholder) {
    char echoCharOriginal = campo.getEchoChar();

    campo.setText(placeholder);
    campo.setEchoChar((char) 0);
    campo.setForeground(Color.gray);

    campo.addFocusListener(new java.awt.event.FocusAdapter() {
        @Override
        public void focusGained(java.awt.event.FocusEvent evt) {
            String texto = new String(campo.getPassword());
            if (texto.equals(placeholder)) {
                campo.setText("");
                campo.setEchoChar(echoCharOriginal);
                campo.setForeground(Color.black);
            }
        }

        @Override
        public void focusLost(java.awt.event.FocusEvent evt) {
            String texto = new String(campo.getPassword());
            if (texto.isEmpty()) {
                campo.setText(placeholder);
                campo.setEchoChar((char) 0);
                campo.setForeground(Color.gray);
            }
        }
    });
}

